package src;

public class ejemploValoresVar {
    public static void main(String[] args) {
        int numero1 = 5;
        int numero2 = 10;
        System.out.println(numero2);

        numero2 = numero1;
        System.out.println(numero2);

        numero1 = 88;
        System.out.println(numero2);
    }
}
